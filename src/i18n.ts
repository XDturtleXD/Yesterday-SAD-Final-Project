export type TranslationKey = keyof typeof translations.en

export function translate(language: LanguagePreference, key: TranslationKey) {
  return translations[language][key] ?? translations.en[key]
}

export function useTranslation() {
  const { language } = useAppState()
  return {
    language,
    t: (key: TranslationKey) => translate(language, key),
  }
}

export function roleLabel(role: UserRole, language: LanguagePreference) {
  return translate(language, role === 'admin' ? 'role.admin' : 'role.regular')
}

export function memberRoleLabel(role: ProjectMemberRole, language: LanguagePreference) {
  return translate(language, `memberRole.${role}` as TranslationKey)
}

export function inviteRoleLabel(role: 'principal' | 'member', language: LanguagePreference) {
  return translate(language, `inviteRole.${role}` as TranslationKey)
}
